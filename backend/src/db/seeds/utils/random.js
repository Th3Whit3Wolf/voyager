const randomName = () => {
	const lastName = lastNames[Math.floor(Math.random() * lastNames.length)];

	if (Math.floor(Math.random()) === 1) {
		return [
			maleFirstNames[Math.floor(Math.random() * maleFirstNames.length)],
			lastName
		];
	}
	return [
		femaleFirstNames[Math.floor(Math.random() * femaleFirstNames.length)],
		lastName
	];
};

const randomDSN = () => {
	const DSN_PREFIXS = [312, 313, 314, 315, 316, 317, 317];
	const areaCode =
		DSN_PREFIXS[Math.floor(Math.random() * DSN_PREFIXS.length)];
	const centralOfficeCode = Math.floor(Math.random() * 899 + 100);
	const stationNumber = Math.floor(Math.random() * 8999 + 1000);

	return `(${areaCode})-${centralOfficeCode}-${stationNumber}`;
};

const randomOfficeSymbol = () => {
	const officeSymbols = ["MSOS", "SCOT", "MSOL", "DOU"];
	return officeSymbols[Math.floor(Math.random() * officeSymbols.length)];
};

